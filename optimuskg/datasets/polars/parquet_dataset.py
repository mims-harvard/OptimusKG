"""``ParquetDataset`` loads/saves data from/to a Parquet file using an underlying
filesystem (e.g.: local, S3, GCS). It uses polars to handle the Parquet file.
"""

from __future__ import annotations

import logging
from copy import deepcopy
from pathlib import Path, PurePosixPath
from typing import Any

import fsspec
import polars as pl
from kedro.io.core import (
    AbstractVersionedDataset,
    DatasetError,
    Version,
    get_filepath_str,
    get_protocol_and_path,
)

from optimuskg.utils import parse_polars_type

logger = logging.getLogger(__name__)


class ParquetDataset(AbstractVersionedDataset[pl.DataFrame, pl.DataFrame]):
    """``ParquetDataset`` loads/saves data from/to a Parquet file using an underlying
    filesystem (e.g.: local, S3, GCS). It uses polars to handle the Parquet file.

    Example usage for the `YAML API <https://docs.kedro.org/en/stable/data/\
    data_catalog_yaml_examples.html>`_:

    .. code-block:: yaml

        cars:
          type: polars.ParquetDataset
          filepath: data/01_raw/company/cars.parquet
          load_args:
            use_pyarrow: true
          save_args:
            compression: zstd

        motorbikes:
          type: polars.ParquetDataset
          filepath: s3://your_bucket/data/02_intermediate/company/motorbikes.parquet
          credentials: dev_s3

    Example usage for the
    `Python API <https://docs.kedro.org/en/stable/data/\
    advanced_data_catalog_usage.html>`_:

    .. code-block:: pycon

        >>> import sys
        >>>
        >>> import polars as pl
        >>> import pytest
        >>> from kedro_datasets.polars import ParquetDataset
        >>>
        >>> if sys.platform.startswith("win"):
        ...     pytest.skip("this doctest fails on Windows CI runner")
        ...
        >>> data = pl.DataFrame({"col1": [1, 2], "col2": [4, 5], "col3": [5, 6]})
        >>>
        >>> dataset = ParquetDataset(filepath=tmp_path / "test.parquet")
        >>> dataset.save(data)
        >>> reloaded = dataset.load()
        >>> assert data.equals(reloaded)

    """

    DEFAULT_LOAD_ARGS: dict[str, Any] = {}
    DEFAULT_SAVE_ARGS: dict[str, Any] = {}
    DEFAULT_FS_ARGS: dict[str, Any] = {
        "open_args_save": {"mode": "w", "encoding": "utf-8"}
    }

    def __init__(  # noqa: PLR0913
        self,
        *,
        filepath: str,
        load_args: dict[str, Any] | None = None,
        save_args: dict[str, Any] | None = None,
        version: Version | None = None,
        credentials: dict[str, Any] | None = None,
        fs_args: dict[str, Any] | None = None,
        metadata: dict[str, Any] | None = None,
    ) -> None:
        """Creates a new instance of ``ParquetDataset`` pointing to a concrete Parquet file
        on a specific filesystem.

        Args:
            filepath: Filepath in POSIX format to a Parquet file prefixed with a protocol
                `s3://`.
                If prefix is not provided, `file` protocol (local filesystem)
                will be used.
                The prefix should be any protocol supported by ``fsspec``.
                Note: `http(s)` doesn't support versioning.
            load_args: Polars options for loading Parquet files.
                Here you can find all available arguments:
                https://pola-rs.github.io/polars/py-polars/html/reference/api/polars.read_parquet.html
                All defaults are preserved.
            save_args: Polars options for saving Parquet files.
                Here you can find all available arguments:
                https://pola-rs.github.io/polars/py-polars/html/reference/api/polars.DataFrame.write_parquet.html
                All defaults are preserved.
            version: If specified, should be an instance of
                ``kedro.io.core.Version``. If its ``load`` attribute is
                None, the latest version will be loaded. If its ``save``
                attribute is None, save version will be autogenerated.
            credentials: Credentials required to get access to the underlying filesystem.
                E.g. for ``GCSFileSystem`` it should look like `{"token": None}`.
            fs_args: Extra arguments to pass into underlying filesystem class constructor
                (e.g. `{"project": "my-project"}` for ``GCSFileSystem``).
                Defaults are preserved, apart from the `open_args_save` `mode` which is set to `wb`.

            metadata: Any arbitrary metadata.
                This is ignored by Kedro, but may be consumed by users or external plugins.
        """
        _fs_args = deepcopy(fs_args) or {}
        _fs_open_args_load = _fs_args.pop("open_args_load", {})
        _fs_open_args_save = _fs_args.pop("open_args_save", {})
        _credentials = deepcopy(credentials) or {}

        protocol, path = get_protocol_and_path(filepath, version)
        if protocol == "file":
            _fs_args.setdefault("auto_mkdir", True)

        self._protocol = protocol
        self._storage_options = {**_credentials, **_fs_args}
        self._fs = fsspec.filesystem(self._protocol, **self._storage_options)

        self.metadata = metadata

        super().__init__(
            filepath=PurePosixPath(path),
            version=version,
            exists_function=self._fs.exists,
            glob_function=self._fs.glob,
        )

        # Handle default load and save and fs arguments
        self._load_args = {**self.DEFAULT_LOAD_ARGS, **(load_args or {})}
        self._save_args = {**self.DEFAULT_SAVE_ARGS, **(save_args or {})}
        self._fs_open_args_load = {
            **self.DEFAULT_FS_ARGS.get("open_args_load", {}),
            **(_fs_open_args_load or {}),
        }
        self._fs_open_args_save = {
            **self.DEFAULT_FS_ARGS.get("open_args_save", {}),
            **(_fs_open_args_save or {}),
        }

        if "storage_options" in self._save_args or "storage_options" in self._load_args:
            logger.warning(
                "Dropping 'storage_options' for %s, "
                "please specify them under 'fs_args' or 'credentials'.",
                self._filepath,
            )
            self._save_args.pop("storage_options", None)
            self._load_args.pop("storage_options", None)

    def _describe(self) -> dict[str, Any]:
        return {
            "filepath": self._filepath,
            "protocol": self._protocol,
            "load_args": self._load_args,
            "save_args": self._save_args,
            "version": self._version,
        }

    def load(self) -> pl.DataFrame:
        load_path = get_filepath_str(self._get_load_path(), self._protocol)
        load_args = self._load_args.copy()

        schema_config = load_args.pop("schema", None)
        if schema_config:
            load_args["schema"] = self._parse_schema(schema_config)

        if self._protocol != "file":
            load_args["storage_options"] = self._storage_options

        return pl.read_parquet(load_path, **load_args)

    def _parse_schema(self, schema: dict[str, Any]) -> dict[str, Any]:
        parsed = {}
        for col_name, type_spec in schema.items():
            try:
                parsed[col_name] = self._parse_type_spec(type_spec)
            except ValueError as e:
                logger.warning("Could not parse type for column '%s': %s", col_name, e)
        return parsed

    def _parse_type_spec(self, type_spec: str | dict | list) -> Any:
        if isinstance(type_spec, str):
            return parse_polars_type(type_spec)
        elif isinstance(type_spec, dict):
            fields = {
                name: self._parse_type_spec(field_spec)
                for name, field_spec in type_spec.items()
            }
            return pl.Struct(fields)
        elif isinstance(type_spec, list):
            if len(type_spec) != 1:
                raise ValueError(
                    f"List schema must have exactly one element, got {len(type_spec)}"
                )
            return pl.List(self._parse_type_spec(type_spec[0]))

    def save(self, data: pl.DataFrame) -> None:
        save_path = get_filepath_str(self._get_save_path(), self._protocol)
        save_args = self._save_args.copy()
        if self._protocol != "file":
            save_args["storage_options"] = self._storage_options

        self._ensure_parent_dir_exists(save_path)
        data.write_parquet(file=save_path, **save_args)

        self._invalidate_cache()

    def _ensure_parent_dir_exists(self, filepath: str) -> None:
        if self._protocol == "file":
            Path(filepath).parent.mkdir(parents=True, exist_ok=True)
        else:
            self._fs.mkdirs(str(Path(filepath).parent), exist_ok=True)

    def _exists(self) -> bool:
        try:
            load_path = get_filepath_str(self._get_load_path(), self._protocol)
        except DatasetError:
            return False

        return self._fs.exists(load_path)  # type: ignore

    def _release(self) -> None:
        super()._release()
        self._invalidate_cache()

    def _invalidate_cache(self) -> None:
        filepath = get_filepath_str(self._filepath, self._protocol)
        self._fs.invalidate_cache(filepath)
